const getContract = (
  isDefault,
  isBurn,
  isMint,
  isPause,
  isBlacklist,
  isDeflation
) => {
  if (isDefault) return { abi: defaultAbi, bytecode: defaultBytecode, code: defaultCode };
  const variantArray = [isBurn, isMint, isPause, isBlacklist, isDeflation];
  const variant = variantArray.reduce(
    (acc, val, index) => (val ? acc + index : acc),
    ""
  );
  
  return variantObject[variant];
};

export default getContract;
